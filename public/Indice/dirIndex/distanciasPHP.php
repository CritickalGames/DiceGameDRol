	Hay 2 tipos de mapas: El mapa MUNDI y el mapa PEQUEÑO.<br>
	En el Pequeño, cada casilla es 1 metro cuadrado.<br>
	En el Mundi, cada casilla son 5 metros cuadrados.<br>
</p>
<p class="li">
	El mapa Pequeño mide 30 metros cuadrados.<br>
	El mapa Mundi mide 6 mapas pequeños al cuadrado. Es decir: 120 metros cuadrados.
