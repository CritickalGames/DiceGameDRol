		<p>HOLA REGLAS:Turnos</p>
		Éste juego va por turnos y se pueden compartir por 2 o más personas.<br>
		Para limitar el tiempo que dura cada turno, los jugadores tienen "15 PA", es decir "15 puntos de acción".<br>
		Todo lo que hagas costará un número de PA<br> 
		La lista es:
		<ul>
			<li class="li">Acciones Gratis<br>
				-Hablar con jugadores: Free
<br>
				-Moverse: Sólo cuesta puntos de movimiento.
<br>
				-Las acciones que puedes hacer gracias a lo salido en los dados: rerol, dado extra, etc.
<br>
				-Cuando te atacan se activa el "DECA": Defender, Esquivar o Contra-Atacar.
<br>
				-Ver a los lados: Puedes ver cualquier cosa que esté a simple vista: si está a menos de 30 metros y no hay obstaculos.
			</li>
			<li class="li">Acciones de valor 1<br>
				-Deseo random: 1 acción. Sólo se puede hacer una vez por turno
<br>
				-Hablar con NPC: 1 Por cada pregunta, cambio de tema o retomar conversación. Esto no aplica a las tiendas.
<br>
				-Tienda de objetos: 1 acción. Se puede abrir cuando quieras
<br>
				-Tienda de cartas azules: 1 acción. Puedes hablar con El Sureño si lo encuentras. 
<br>
				-Tomar objetos: 1 acción
<br>
				-Usar objeto: 1 acción
			</li>
			<li class="li">Acciones de valor 2<br>
				-Reacciones: Declarar cualquier reacción te vale 2. Si quieres activarla te vale mitad de la acción declarada; redondeo hacia abajo. Se puede declarar un máximo de 3 reacciones.
<br>
				-Des/Activar Don/Poder: Los SPS, los simbiontes, habilidades de armas[pistola de portales], transformaciones, invocar armas.
			</li>
			<li class="li">Acciones de valor 3<br>
				-Usar objetos curativos, curarte de formas normales, curar a alguien más.
			</li>
			<li class="li">Acciones de valor 4<br>
				-Usar Don/Poder: Toda habilidad sobre natural: velocidad, defensa, reflejos, aumentar precisión, rerol, aplicar efectos de de/buffo[a ti o a otros].
<br>
				Si tu habilidad te da una ventaja en el combate, puedes activarla después de impactar o en cualquier momento de tu turno.
<br>
				-Buscar o intentar ver algo através de obstaculos/a más de 30 metros
			</li>
			<li class="li">Acciones de valor 5<br>
				-Curarte con simbiontes y poderes propios.
<br>
				_No te puedes curar automticamente sin gastar puntos, pero ese tipo de regeneración vale 4
<br>
				_Todo lo que forme parte de tu organismo, alma(excepto stands a no ser que sea el poder), un pacto con tu alma, o directamente sea un poder, cuesta 5.
<br>
				_No existe regeneración base natural.
			</li>
			<li class="li">Acciones de valor 7<br>
				REACCIÓN_MEGA_DEFINITIVA: esquivas y atacas= reacción(7 acciones para activar); tira 1d20 Si saca 15 o más, ganas, si no, recibe el impacto sin defensa alguna. No puedes reducir el daño con nada, ni tú ni el enemigo.<br>
				Si superas el DC 15, haces el mismo ataque el enemigo y él recibe su propio golpe.<br>
				_Sólo se puede hacr si la aprendes previamente o la compras por cartas blancas.
			</li>
			<li class="li">Acciones de valor 10<br>
				-Atacar: Se considera "atacar" hacer algo con el cuerpo de un jugador en contra de su voluntad.
<br>
				-Atacar con stand: 10. Sólo si atacas cuerpo a cuerpo con el stands.(Moverlo de un lado a otro cuesta 1)
				
			</li>
			<li class="li">Acciones de valor especial<br>
				-Activar reacción: la mitad de la acción declarada.
<br>
				-Dar una orden a un SPS: Los SPS se mueven con tus acciones. Cada orden que les des cuesta 1 y usar sus habilidades cuesta 4 aunque sea de modo agresivo. Atacar con Stans cuesta 10, no 11.
<br>
				-Estudiar/practicar una tecnica: dependiendo de la tecnica(el control de energía es tecnica), vas a tener que gastar X turnos estudiando. Se puede parar y volver a estudiar en cualquier momento.
<br>
				-Acumular energía: No se puede parar hasta llegar al mínimo.
<br>
				-Algunas habilidades: Ciertas habilidades serán equilibradas subiendo o bajando los PA que consume.
			</li>
		
		Actuar fuera de tuerno:<br>
		Sólo puedes actuar fuera de turno con tus reacciones, cuando te atacan.
		<li class="li"><center>RECCIONAR</center><br>
			-Las reacciones son acciones preparadas para ejecutarse fuera del tuerno. ¿Cómo sé qué tipo de reacciones puedo hacer?.<br>
			-Eso es fácil: si la acción declarada entra en el DECA, funciona como lo declaraste.<br>
			-No puedes poner condiciones como "Le Tiro al suelo y cuando está en el suelo, le mato".<br>
			-Tú no eliges si le matas, sólo lo intentas. Si se te pregunta "¿Lo matas o dejas vivir?" Ahí sí.<br>
			Puedes declarar condiciones como "Si ocurre esto, intento aquello".<br>
			-Si la reacción es DECA: tiras con ventaja.
		</li>
		</ul>
