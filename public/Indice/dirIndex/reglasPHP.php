<div class="body" id="rules" >
	<ul>
		<li class="li">
			Cada jugador inicia con 15 PA(puntos de acción).
		</li>
		<li class="li">
			La única muerte instantanea es el 13.
		</li>
		<li class="li">
			Si ocurre algo que podrían ver de casualidad o oir, tiran su reacción.
		</li>
		<li class="li">
			El juego se basa en 2 sistemas y 3 tipos de dados.
		</li>
		<li class="li">
			DADOS<br>
			Generalmente se usa xD6 y es el sistema base.<br>
			Las cosas que son más debiles que un 1d6 se usa el xD4.<br>
			1D20. Sólo se usa con la reacción definitiva.
		</li>
		<li class="li">
			SISTEMA DE ÉXITOS<br>
			El juego usa común mente el D6. Cuando se tira un xD6, se cuentan cuantos numeros superiores a 3 salierón. Cada uno es un éxito, el 6 son 2 éxitos y el 1 es -1 éxitos.
		</li>
		<li class="li">
			SISTEMA DE PUNTOS<br>
			El juego, en combate, compara quien tiene un número mayor para definir si el ataque impacta o no y suma los puntos en el DAÑO para ver cuanto daño causa el impacto.
		</li>
		<li class="li">
			PELEAS<br>
			Es simple:<br>
			Gastas 10 PA, declaras a quien y con qué atacas a alguien.<br>
			_Tiras tu +ATK, le aplicas las habilidades que puedas.<br>
			_Él tira su +DEF/+DEA.<br>
			_Compruebas quien sacó critico o +ATK>+DEF.<br>
			_+ATK>+DEF o sólo tú sacasté critico, tiras tu DAÑO.<br>
			También aplica para atrapar o obligar a hacer algo a alguien.
		</li>
		<li class="li">
			EMPATES<br>
			Si dos personas embatan en sus tiradas, quien defiende gana.
		</li>
		<li class="li">
			CRITICO<br>
			Significa hacer algo perfecto y ocurre cuando todos tus dados tirados son 6.
		</li>
		<li class="li">
			PIFIA<br>
			Significa que te sale el tiro por la culata. Si te intentas curar y sale pifia, te latismas, por ejemplo. Se consigue cuando en el recuento de éxitos consigues -1 éxitos o menos.
		</li>
		<li class="li">
			FALLAR<br>
			No significa hacer las cosas mal, significa no hacerlas bien. Si te intentas curar y fallas, no te lastimas; por ejemplo. Se consigue al no llegar al número mínimo de éxitos. 
		</li>
		<li class="li">
			MULTIPLICADORES<br>
			Cuando en una tirada sacas un par de numeros iguales, ganas un X2 de daño.
		</li>
	</ul>
</div>