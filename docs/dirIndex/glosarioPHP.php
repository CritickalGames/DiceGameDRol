<div class="body" id="glosario">
		<p>HOLA REGLAS:Glosario</p>
	<ul>
		<li class="li">
			X% o xDx%= X puede ser cualquier número que va a afectar como suma o resta según el contexto donde se mensione. Ejemplo: curarse 10%. X es 10.<br>
			xDx% siendo x cualquier número, significa que tiras una cantidad indeterminados de dades de un números de caras indeterminados.<br>
			Sólo se aplican a las cosas que tengan relaión con el %
			xdx= afecta al impacto, la defensa o el esquive.
		</li>
		<li class="li">
			HP=vida. Se dice que es procentual
		</li>
		<li class="li">
			Ataque= Agredir a otra entidad; empujar, pegar, agarrar, etc al otro.
		</li>
		<li class="li">
			Impactar= Después de declarar un Ataque, se comprueba si se Impacta comparando la +DEF o +ATK. 
		</li>
		<li class="li">
			ATK%= Es un extra al daño o el daño en sí. Puede ser un X% o xDx% cuando haces daño, después de impactar.
		</li>
		<li class="li">
			+ATK= Sirve para comprobar si impactas. Es tu presición al golpear. Sólo puede ser xDx. 
		</li>
		<li class="li">
			DEF%= Es defensa física que te reduce el daño cuando te Impactan. Puede ser X% o xDx%. 
		</li>
		<li class="li">
			+DEF= Es defensa física que te reduce la probabilida de Impacto. La explicación es que estás tan duro que las balas rebotan. Sólo puede ser xDx.
		</li>
		<li class="li">
			+DEA= Es tu capacidad de esquive. Puedes ganarla con reacción o MOV
		</li>
		<li class="li">
			xPA y PA= Puntos de acción. La X indica que la acción tendrá su coste personalizado.
		</li>
		<li class="li">
			DESEO= es una acción que ocurre en tu turno. Tiras 2d6 y ves la lista del deseo para ver qué te salió.
		</li>
		<li class="li">
			MOV= Movimiento, es la cantidad de casillas/metros que te puedes desplazar en el turno.
		</li>
		<li class="li">
			DECA= Siglas de "Defensa, Esquive, Contra-Ataque". Representa las acciones que haces cuando te agreden.
		</li>
		<li class="li">
			SPS= Stand, Pokémon o Servants.
		</li>
		<li class="li">
			Ventaja= Tiras dos veces y te quedas con el resultado mayor.
			Desventaja= Tiras dos veces y te quedas on el resultado menor.
		</li>
		<li class="li">
			Critico= Es cuando al tirar tu +DEF/+DEA o +ATK, todos los dados que lanzas tienen un 6.<br>
			Los escudos no afectan al número de seises que haz de sacar.<br>
			El Critico significa hacer algo de forma exelente y si tiras tus dados para intentar otro tipo de pruebas, vas a hacer la prueba de forma perfecta.
		</li>
		<li class="li">
			Pifia= significa sacar -1 éxitos o menos en tu tiradas.
		</li>
		<li class="li">
			Arquetipo= indica qué tipos de poderes te saldrán en el 4 o podrás comprar con cartas.
		</li>
	</ul>
</div>