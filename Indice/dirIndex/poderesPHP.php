		<p>HOLA REGLAS:crear poderes</p>
		<center><h2>Para crear nuevos poderes</h2></center>
		<p>Para crear un nuevo poder NO DIGAS QUE DESTRUYE EL DIAMANTE PORQUE ESE TITULO, aún, NO TIENE EN LAS REGLAS DEL JUEGO</p>
		<ul>
			<li class="li">
				DAÑO= dale xD6 que se le sumaran al daño que haga.Puede ser activa o pasiva la habilidad.<br>
				Las habilidades de movimiento rápido también le pueden dar DAÑO<br>
				Simbolo: ATK%=xDx%
			</li>
			<li class="li">
				DAÑO%= puedes darle un plus porcentual de daño.<br>
				El daño puede ir de 1%~10%<br>
				Simbolo: ATK%= X%
			</li>
			<li class="li">
				ATAQUE= dale xD6 que se le sumaran a cada ataque o contra ataque. Puede ser activas o pasiva la habilidad.<br>
				Las habilidades de movimento le pueden dar ATAQUE, pero si no está corriendo.En caso de correr, primero tiene que tirar reacción. Si salva: puede tira con ventaja, en caso de errar, tira con deventaja<br>
				Simbolo: +ATK=xDx
			</li>
			<li class="li">
				DEFENSA FÍSICA= dale xD6 que se sumarán a sus bloqueos. Puede ser activa o pasiva la habilidad.<br>
				Las habilidades de movimiento no le afectan positiva o negativamente. Las de regeneración da DEF% <br>
				También sirve para aguantar daño. Si te impactan, reduces el daño xD6%.<br>
				Simbolo: +DEF=xDx, DEF%=xDx o DEF%=X%
			</li>
			<li class="li">
				DEFENSA ÁGIL= dale xD6 que se sumarán a su esquive o contra ataque. Puede ser activa o pasiva la habilidad. También pueden ser o no usadas fuera de turno. Eso depende de la habilidad. <br>
				La habilidades pueden ayudarle, pero muy poco, lo importante son los reflejos.<br>
				Simbolo: +DEA=xDx
			</li>
			<li class="li">
				Armaduras= Es defensa porsentual pura. Se le suma a +DEF%. Puedes supar desde 1% a 20% por poder.<br>
				Simbolo: DEF%=X%
			</li>
			<li class="li">
				Cura= xDx%; Te curas xDx%.
				Cura_Instantanea= DEF%=xDx
				Cura= HP+DAÑO*cura%; Te cura X% del último daño recibido.<br>
				Cura_Absoluta=HP+DAÑO.TOTAL*cura%; Te cura un porcentaje del daño total.<br>
			</li>
			<li class="li">
				Escudos= +DEF. No sube la cantidad de dados que necesitas para el critico. 
			</li>		
			<li class="li">
				REACCIÓN_MEGA_DEFINITIVA:esquivas y atacas= reacción(7 acciones); tira 1d20 Si saca 15 o más, gana, si no, recibe el impacto sólo con defensa base. TODA ARMADURA INÚTIL PARA AMBOS.<br>
			</li>
			<li class="li">
				Habilidades de estudio: Te da xPA cada vez que empiezas a estudiar algo. Sólo se aplica a lo estudiado<br>
				Aprender más rápido: Cada vez que gastas xPA, estudiando una misma habilidad, se te da xPA extra a lo estudiado.
			</li>
		</ul>
