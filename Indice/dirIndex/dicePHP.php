		<p >HOLA REGLAS: acomular "dice X"</p>
		Existen algo llamado DICE X y son intangibles.Hay 2 DICE:<br>
	<p class="li">
		Racha-<br>
		-DICE 6: Se acomulan sacando 6 en el DESEO o 6 en los dados. Se acumulan como una racha, y si tu próximo DESEO no es un 6 o tu próxima tirada no tiene un 6 en uno de los dados de 6 caras, pierdes la racha y vuelves a 6.
		<br>
		-Los reroles te eliminan el último 6 obtenido si se re-rolea el dado con el 6.
		<br>
		-Si acumulas 3 DICE 6, giras la ruleta de demonios una vez y tienes derecho a un pacto. Es como tener un DESO 18.
		<br>
		-Si acumulas 4 DICE 6, es como tener un DESEO 24. Inmune a un ataque mortal.<br>
		-Si tiras, por ejemplo, 2d6=1, 6. No se rompe la racha al haber un 6 en la tirada.
	</p>
	<p class="li">
		Acumulativos-<br>
		-Estos DICE vivenen sólo con los DESEOs.<br>
		-DICE 8: Re-Roleas un dado.<br>
		-Dice 10: Agregas un dado a cualquier tirada.<br>
		-Son acumulativos y se pueden usar gratis en cualquier momento.<br>
